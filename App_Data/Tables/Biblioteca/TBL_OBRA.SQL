CREATE TABLE TBL_OBRA
(
	COD_OBRA INT IDENTITY,
	COD_TIPO_OBRA INT,
	CHR_CHAMADA VARCHAR(16) NOT NULL,
	CHR_TITULO VARCHAR(512) NOT NULL,
	COD_AUTOR_OBRA INT,
	CHR_EDICAO VARCHAR(16),
	CHR_PUBLICACAO VARCHAR(64),
	CHR_NORMALIZACAO VARCHAR(128),
	COD_ASSUNTO_OBRA INT,
	COD_STATUS_OBRA INT,
	
	CONSTRAINT PK_OBRA PRIMARY KEY (COD_OBRA),
	CONSTRAINT UN_OBRA_CHAMADA UNIQUE (CHR_CHAMADA),
	CONSTRAINT FK_OBRA_TIPO_OBRA FOREIGN KEY (COD_TIPO_OBRA) REFERENCES TBL_TIPO_OBRA (COD_TIPO_OBRA),
	CONSTRAINT FK_OBRA_AUTOR_OBRA FOREIGN KEY (COD_AUTOR_OBRA) REFERENCES TBL_AUTOR_OBRA (COD_AUTOR_OBRA),
	CONSTRAINT FK_OBRA_ASSUNTO_OBRA FOREIGN KEY (COD_ASSUNTO_OBRA) REFERENCES TBL_ASSUNTO_OBRA (COD_ASSUNTO_OBRA),
	CONSTRAINT FK_OBRA_STATUS_OBRA FOREIGN KEY (COD_STATUS_OBRA) REFERENCES TBL_STATUS_OBRA (COD_STATUS_OBRA)
)