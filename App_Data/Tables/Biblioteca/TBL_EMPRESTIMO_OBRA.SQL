CREATE TABLE TBL_EMPRESTIMO_OBRA
(
    COD_EMPRESTIMO_OBRA INT IDENTITY,
    COD_MORADOR INT,
    COD_OBRA INT,
    DT_DATA_EMPRESTIMO SMALLDATETIME,
    DT_DATA_DEVOLUCAO SMALLDATETIME,
    DT_DATA_DEVOLUCAO_PREVISTA SMALLDATETIME,
    INT_CONT_RENOVACAO INT,
    BLN_ABERTO BIT
    
    CONSTRAINT PK_EMPRESTIMO_OBRA PRIMARY KEY (COD_EMPRESTIMO_OBRA),
    CONSTRAINT FK_EMPRESTIMO_OBRA_MORADOR FOREIGN KEY (COD_MORADOR) REFERENCES TBL_MORADOR (COD_MORADOR),
    CONSTRAINT FK_EMPRESTIMO_OBRA_OBRA FOREIGN KEY (COD_OBRA) REFERENCES TBL_OBRA (COD_OBRA)
)