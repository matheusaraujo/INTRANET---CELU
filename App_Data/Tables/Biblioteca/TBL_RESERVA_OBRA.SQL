CREATE TABLE TBL_RESERVA_OBRA
(
    COD_RESERVA_OBRA INT IDENTITY,
    COD_MORADOR INT,
    COD_OBRA INT,
    DT_DATAHORA SMALLDATETIME,
    BLN_ABERTA BIT
    
    CONSTRAINT PK_RESERVA_OBRA PRIMARY KEY (COD_RESERVA_OBRA),
    CONSTRAINT FK_RESERVA_OBRA_MORADOR FOREIGN KEY (COD_MORADOR) REFERENCES TBL_MORADOR (COD_MORADOR),
    CONSTRAINT FK_RESERVA_OBRA_OBRA FOREIGN KEY (COD_OBRA) REFERENCES TBL_OBRA (COD_OBRA)
)