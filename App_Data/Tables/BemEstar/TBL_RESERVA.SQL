CREATE TABLE TBL_RESERVA
(
    COD_RESERVA INT IDENTITY,
    COD_ESPACO_RESERVA INT,
    COD_STATUS_RESERVA INT,
    DT_DATA_HORA_INICIAL SMALLDATETIME,
    DT_DATA_HORA_FINAL SMALLDATETIME,
    CHR_EVENTO VARCHAR(256),
    INT_QUANTIDADE INT,
    CHR_RESPONSAVEL VARCHAR(256),
    CHR_CONTATO VARCHAR(256),
    CHR_ORIGEM VARCHAR(256),
    CHR_OBSERVACAO VARCHAR(1024),
    BLN_RESERVA_EXCLUSIVA BIT,
    DT_DATA_DA_RESERVA SMALLDATETIME,
    CHR_RESPONSAVEL_RESERVA VARCHAR(256),
    
    CONSTRAINT PK_RESERVA PRIMARY KEY (COD_RESERVA),
    CONSTRAINT FK_RESERVA_ESPACO_RESERVA FOREIGN KEY (COD_ESPACO_RESERVA) REFERENCES TBL_ESPACO_RESERVA (COD_ESPACO_RESERVA),
    CONSTRAINT FK_RESERVA_STATUS_RESERAV FOREIGN KEY (COD_STATUS_RESERVA) REFERENCES TBL_STATUS_RESERVA (COD_STATUS_RESERVA)
)