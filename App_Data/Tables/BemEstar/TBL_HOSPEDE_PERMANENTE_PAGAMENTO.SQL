CREATE TABLE TBL_HOSPEDE_PERMANENTE_PAGAMENTO
(
    COD_HOSPEDE_PERMANENTE_PAGAMENTO INT IDENTITY,
    COD_HOSPEDE_PERMANENTE INT,
    INT_MES INT,
    INT_ANO INT,
    FLT_VALOR NUMERIC(10,2),
    DT_DATA_PAGAMENTO SMALLDATETIME,
    CHR_OBSERVACAO VARCHAR(1024)
    
    CONSTRAINT PK_HOSPEDE_PERMANENTE_PAGAMENTO PRIMARY KEY (COD_HOSPEDE_PERMANENTE_PAGAMENTO),
    CONSTRAINT FK_HOSPEDE_PERMANENTE_PAGAMENTO_HOSPEDE_PERMANENTE FOREIGN KEY (COD_HOSPEDE_PERMANENTE) REFERENCES TBL_HOSPEDE_PERMANENTE (COD_HOSPEDE_PERMANENTE),
    CONSTRAINT UN_HOSPEDE_PERMANENTE_PAGAMENTO_MES_ANO UNIQUE (INT_MES, INT_ANO)
)