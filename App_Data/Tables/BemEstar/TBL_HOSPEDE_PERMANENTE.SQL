CREATE TABLE TBL_HOSPEDE_PERMANENTE
(
    COD_HOSPEDE_PERMANENTE INT IDENTITY,
    COD_QUARTO INT,
    CHR_NOME VARCHAR(256),
    CHR_RG VARCHAR(16),
    CHR_CPF VARCHAR(16),
    COD_CIDADE_ORIGEM INT,    
    CHR_CURSO VARCHAR(256),
    CHR_RECEPCAO VARCHAR(256),
    BLN_ATIVO BIT,
    
    CONSTRAINT PK_HOSPEDE_PERMANENTE PRIMARY KEY (COD_HOSPEDE_PERMANENTE),
    CONSTRAINT FK_HOSPEDE_PERMANENTE_CIDADE_ORIGEM FOREIGN KEY (COD_CIDADE_ORIGEM) REFERENCES TBL_CIDADE (COD_CIDADE),    
    CONSTRAINT FK_HOSPEDE_PERMANENTE_QUARTO FOREIGN KEY (COD_QUARTO) REFERENCES TBL_QUARTO (COD_QUARTO)    
)